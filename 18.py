#Calcular la velocidad de un móvil que se desplaza con velocidad constante conociendo el
#espacio recorrido y el tiempo empleado en recorrerle (los datos serán leídos al comenzar el
#programa)

recorrido = float(input("Ingresa el espacio recorrido: "))
tiempo = float(input("Ingresa el tiempo: "))

velocidad = recorrido / tiempo

print(f"La velocidad constante fue: {velocidad}")
